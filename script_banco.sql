create database formulariotcc;

create table formulario(
tipo_usuario varchar(50) not null,
nome varchar(50) not null,
idade int not null,
frequencia varchar(500) not null,
funcao varchar(500) not null,
problema varchar(500) not null,
aprender varchar(3) not null,
explorar varchar(3) not null,
aprender_animais varchar(3) not null,
funcionalidas_uteis varchar(3) not null
);



